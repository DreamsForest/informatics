from task_base import BaseTaskPage
import random


class Task20Page(BaseTaskPage):
    def __init__(self, main_app, **kwargs):
        super().__init__(main_app, "task_20", **kwargs)

    def generate_task(self):
        """Генерирует задание №20 - Выигрышная стратегия с двумя кучами"""
        S1 = random.randint(5, 15)  # первая куча
        S2 = random.randint(5, 15)  # вторая куча

        self.current_task = {
            'description': f'''Два игрока играют с двумя кучами камней. 
За один ход игрок может:
- добавить 1 камень в любую кучу ИЛИ
- увеличить количество камней в любой куче в два раза.
Игра завершается, когда суммарное количество камней в двух кучах становится не менее {S1 + S2 + 10}.''',
            'question': f'''Определите, кто выигрывает при правильной игре: первый или второй игрок? 
(Начальные размеры куч: {S1} и {S2})''',
            'answer': f"Анализ выигрышной стратегии для куч ({S1}, {S2})"
        }